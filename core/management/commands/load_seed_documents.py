class Command(BaseCommand):
    help = "Load a seed set of real Wildberries/Ozon seller-policy documents for the RAG demo."

    def handle(self, *args, **options):
        created = 0
        for entry in SEED_DOCUMENTS:
            _, was_created = Document.objects.update_or_create(
                title=entry["title"],
                defaults={"source": entry["source"], "content": entry["content"]},
            )
            created += int(was_created)
        self.stdout.write(
            self.style.SUCCESS(
                f"Seeded {len(SEED_DOCUMENTS)} documents ({created} newly created)."
            )
        )
